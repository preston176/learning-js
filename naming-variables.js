//Naming Variables
/*
Variable names
-cannot contain spaces.
-must begin with a letter,
    an underscore (_) or a dollar sign ($)
-names can only contain letters, numbers, underscores or $
-names are case-sensitive
-they cannot use reserved keywords
eg const, catch, var etc..

## its recommended to use camelCase convention during naming
*/

